public class changechar {

	public static void main(String[] args) {
		System.out.println("北京\t天津\t上海");//北京    天津    上海
		System.out.println("it is very \nbeautiful");//it is very 
		//beautiful
		System.out.println("this is a \\");//this is a \
		System.out.println("\"老师说要好好学习\"");//"老师说要好好学习"
		System.out.println("he  like me\rshe");//she like me
		//练习
		System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
	}
}